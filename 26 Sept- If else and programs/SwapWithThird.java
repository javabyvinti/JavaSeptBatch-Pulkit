package javaseptbatch;

public class SwapWithThird {

	public static void main(String[] args) {
	
		int value1 = 10;
		int value2 = 20;
		int temp = 0;
		
		temp = value1;//temp = 10, value1 = 10
		value1 = value2;//value1 = 20
		value2 = temp;//value2 = 10
		
		System.out.println("Value1 is= " +value1 + " Value2 is= " + value2);
		//value1 = 20;
		//value2 = 10;

	}

}
